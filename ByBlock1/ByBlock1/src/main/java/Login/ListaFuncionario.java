    private void BPreencherActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BPreencherActionPerformed
       try {
            DefaultTableModel modelo = (DefaultTableModel) tabela.getModel();
            if (modelo.getRowCount() > 0) {
                modelo.setRowCount(0);
            }

            String dir = System.getProperty("user.home")+"\\Desktop";
            String nomeArq = "funcionarios.csv";
            File arquivo = new File(dir, nomeArq);

            FileInputStream fis = new FileInputStream(arquivo);
            InputStreamReader isr = new InputStreamReader(fis);
            BufferedReader br = new BufferedReader(isr);

            String linha = br.readLine();
            while (linha != null) {
                Object[] obj = linha.split(";");
                modelo.addRow(obj);
                linha = br.readLine();
            }
        } catch (FileNotFoundException e) {
            JOptionPane.showMessageDialog(null, "Arquivo n√£o encontrado!!");
        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, "ERRO");
        }
    }//GEN-LAST:event_BPreencherActionPerformed

